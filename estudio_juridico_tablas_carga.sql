DROP DATABASE IF EXISTS estudio_juridico;
CREATE DATABASE estudio_juridico CHARACTER SET utf8mb4;
USE estudio_juridico;

CREATE TABLE cliente(
id_cliente INT NOT NULL,
nombre VARCHAR(30) NOT NULL,
apellido VARCHAR(30) NOT NULL,
dni INT UNIQUE NOT NULL,
direccion VARCHAR(50) NOT NULL,
telefono INT NOT NULL,
mail VARCHAR(50) NOT NULL,
PRIMARY KEY (id_cliente)
);

CREATE TABLE abogado(
id_abogado INT NOT NULL,
nombre VARCHAR(30) NOT NULL,
apellido VARCHAR(30) NOT NULL,
matricula INT UNIQUE NOT NULL,
especialidad VARCHAR(50) NOT NULL,
telefono INT NOT NULL,
mail VARCHAR(50) NOT NULL,
PRIMARY KEY (id_abogado)
);

CREATE TABLE caso(
id_caso INT NOT NULL,
nro_expediente INT UNIQUE NOT NULL,
estado VARCHAR(50) NOT NULL,
fecha_inicio DATE NOT NULL,
fecha_cierre DATE DEFAULT NULL,
id_cliente INT NOT NULL,
PRIMARY KEY (id_caso),
FOREIGN KEY (id_cliente) REFERENCES cliente (id_cliente)
);

CREATE TABLE audiencia(
id_audiencia INT NOT NULL,
fecha_hora DATETIME  NOT NULL,
lugar VARCHAR(50) NOT NULL,
observacion VARCHAR(100) DEFAULT NULL,
id_caso INT NOT NULL,
PRIMARY KEY (id_audiencia),
FOREIGN KEY (id_caso) REFERENCES caso (id_caso)
);

CREATE TABLE caso_abogado(
id_caso_abogado INT NOT NULL,
rol VARCHAR(30) DEFAULT NULL,
id_caso INT NOT NULL,
id_abogado INT NOT NULL,
PRIMARY KEY (id_caso_abogado),
FOREIGN KEY (id_caso) REFERENCES caso (id_caso),
FOREIGN KEY (id_abogado) REFERENCES abogado (id_abogado)
);

CREATE TABLE documento(
id_documento INT NOT NULL,
nombre_archivo VARCHAR(30) NOT NULL,
fecha_carga DATE NOT NULL,
observacion VARCHAR(100) DEFAULT NULL,
id_caso INT NOT NULL,
PRIMARY KEY (id_documento),
FOREIGN KEY (id_caso) REFERENCES caso (id_caso)
);

CREATE TABLE honorario(
id_honorario INT NOT NULL,
fecha DATE NOT NULL,
monto NUMERIC(15,2) DEFAULT NULL,
estado_pago ENUM ('Pago' , 'No pago') NOT NULL,
id_caso INT NOT NULL,
PRIMARY KEY (id_honorario),
FOREIGN KEY (id_caso) REFERENCES caso (id_caso)
);

ALTER TABLE cliente
MODIFY COLUMN telefono VARCHAR(30);

ALTER TABLE abogado
MODIFY COLUMN telefono VARCHAR(30);

/*************** Index ***************/
CREATE UNIQUE INDEX idx_id_cliente ON cliente (id_cliente);
CREATE INDEX idx_id_abogado ON abogado (id_abogado);


